                <div class="form-group">
                    <label for="nama_kategori">Nama Kategori</label>
                    <input type="text" id="nama_kategori" name="nama_kategori" placeholder="Contoh: Kursi, Meja, Dekorasi" value="<?php echo htmlspecialchars($nama_kategori ?? ''); ?>" required>
                </div>

